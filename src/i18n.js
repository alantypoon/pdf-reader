/** Resolve the UI language — bilingual resolves to English for UI chrome */
export function uiLang(selectedLanguage) {
  return selectedLanguage === 'bilingual' ? 'en' : selectedLanguage;
}

/** Translate a key for the given language */
export function t(key, lang) {
  const dict = T[lang] || T.en;
  return dict[key] || T.en[key] || key;
}

